        <!-- JavaScript (Handles AI Responses) --> 
        <script> 
          document.addEventListener("DOMContentLoaded", function() {
            const chatWindow = document.getElementById("chatWindow");
            const toggleChatButton = document.getElementById("toggleChat");
            const closeChatButton = document.getElementById("closeChat");
            const chatMessages = document.getElementById("chatMessages");
            const userInput = document.getElementById("userInput");

            // Show chat window
            toggleChatButton.addEventListener("click", function() {
                chatWindow.classList.add("active");
                toggleChatButton.style.display = "none";
            });

            // Close chat window
            closeChatButton.addEventListener("click", function() {
                chatWindow.classList.remove("active");
                setTimeout(() => {
                    toggleChatButton.style.display = "block";
                }, 400);
            });

            // Function to add a message
            function addMessage(sender, message) {
                const messageElement = document.createElement("p");
                messageElement.innerHTML = `<strong>${sender}:</strong> ${message}`;
                chatMessages.appendChild(messageElement);

                // Auto-scroll to the bottom
                chatMessages.scrollTop = chatMessages.scrollHeight;
            }

            // Send message to Dialogflow Webhook
            async function sendMessageToAI(message) {
                try {
                    const response = await fetch("chat-webhook.php", { 
                        method: "POST",
                        headers: { "Content-Type": "application/json" },
                        body: JSON.stringify({ query: message })
                    });

                    const data = await response.json();
                    return data.reply || "Sorry, I didn't understand that.";
                } catch (error) {
                    console.error("Error fetching AI response:", error);
                    return "Error: Unable to reach AI server.";
                }
            }

            // Send message when Enter is pressed  
            userInput.addEventListener("keypress", async function(event) {
                if (event.key === "Enter") {
                    event.preventDefault(); // Prevents line break in input
                    const message = userInput.value.trim();
                    if (message === "") return;

                    // Show user message
                    addMessage("Me", message);
                    userInput.value = "";

                    // Get AI response
                    const aiResponse = await sendMessageToAI(message);

                    // Show AI response
                    addMessage("AI", aiResponse);  
                }
            });
        });

        </script>  
        

       

